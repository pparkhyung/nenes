package command;

import java.util.List;

public class Message {

	private String msg;
	private List<String> agent;

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public List<String> getAgent() {
		return agent;
	}

	public void setAgent(List<String> agent) {
		this.agent = agent;
	}

}
